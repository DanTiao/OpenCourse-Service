package com.cc.open.domain;

public class TeachingInfo {
    private String teachingInfoUuid;

    private String teachingInfoTitle;

    private String teachingInfoPublished;

    private String teachingInfoContent;

    public String getTeachingInfoUuid() {
        return teachingInfoUuid;
    }

    public void setTeachingInfoUuid(String teachingInfoUuid) {
        this.teachingInfoUuid = teachingInfoUuid;
    }

    public String getTeachingInfoTitle() {
        return teachingInfoTitle;
    }

    public void setTeachingInfoTitle(String teachingInfoTitle) {
        this.teachingInfoTitle = teachingInfoTitle;
    }

    public String getTeachingInfoPublished() {
        return teachingInfoPublished;
    }

    public void setTeachingInfoPublished(String teachingInfoPublished) {
        this.teachingInfoPublished = teachingInfoPublished;
    }

    public String getTeachingInfoContent() {
        return teachingInfoContent;
    }

    public void setTeachingInfoContent(String teachingInfoContent) {
        this.teachingInfoContent = teachingInfoContent;
    }
}