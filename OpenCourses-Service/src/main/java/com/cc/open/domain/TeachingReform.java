package com.cc.open.domain;

public class TeachingReform {
    private String teachingReformUuid;

    private String teachingReformTitle;

    private String teachingReformPublished;

    private String teachingReformUrl;

    public String getTeachingReformUuid() {
        return teachingReformUuid;
    }

    public void setTeachingReformUuid(String teachingReformUuid) {
        this.teachingReformUuid = teachingReformUuid;
    }

    public String getTeachingReformTitle() {
        return teachingReformTitle;
    }

    public void setTeachingReformTitle(String teachingReformTitle) {
        this.teachingReformTitle = teachingReformTitle;
    }

    public String getTeachingReformPublished() {
        return teachingReformPublished;
    }

    public void setTeachingReformPublished(String teachingReformPublished) {
        this.teachingReformPublished = teachingReformPublished;
    }

    public String getTeachingReformUrl() {
        return teachingReformUrl;
    }

    public void setTeachingReformUrl(String teachingReformUrl) {
        this.teachingReformUrl = teachingReformUrl;
    }
}